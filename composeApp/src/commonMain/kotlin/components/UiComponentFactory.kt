                    NavigationDrawerItem(
                        label = { Text(text = "Drawer Item") },
                        selected = false,
                        onClick = { /*TODO*/ }
                    )
                    // ...other drawer items
                }
            }
        ) {
            // Screen content
            UiComponentFactory().createElevatedCard()
        }
        // [END android_compose_layout_material_modal_drawer]
    }


